import java.util.*;
public class While_Loop {
    public static void main(String[] args) {

        int i = 1;

        while (i <= 10) {
            System.out.println(i);
            i++;
        }
    }
}

/*
DECLARE i : INTEGER
SET i := 1
WHILE i <= 10
    PRINT i
    INCREMENT i
END WHILE
*/
